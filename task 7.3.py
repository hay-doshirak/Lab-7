a = ("Понедельник", "Вторник", "Среда", "Четверг", "Пятница", "Суббота", "Воскресенье")
k = int(input("Введите нужное Вам количество выходных дней: "))
if 0 < k <= 7:
    rest = [a[-i] for i in range(1, k+1)]
    rest.reverse()
    work = [a[i] for i in range(len(a)-k)]
    print("Ваши рабочие дни: ", work)
    print("Ваши выходные дни: ", rest)
else: print("В неделе только 7 дней(")